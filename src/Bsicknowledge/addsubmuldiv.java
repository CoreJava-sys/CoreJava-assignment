public class addsubmuldiv {
	
	
	public static void add() {
			int a1=200;
			int a2=200;
			int sum=a1+a2;
			System.out.println(sum);
	}
		
		public static void sub() {
			int a1=200;
			int a2=200;
			int sum=a1-a2;
			System.out.println(sum);
		}
		public static void mul() {
			int a1=200;
			int a2=200;
			int sum=a1*a2;
			System.out.println(sum);
		}
		public static void div() {
			int a1=200;
			int a2=200;
			int sum=a1/a2;
			System.out.println(sum);
		}
		public static void main(String[] args)
		{
			add();
			sub();
			mul();
			div();
			
		}
		

}
